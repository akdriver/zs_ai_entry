"""
封装/封包 python 中等式的右边出现都好分割多个值的时候，会将数据封装到元组中如下

解包/解构 Python 中右边是一个容器，左边是逗号分隔的多个变量，左侧的变量数和右边容器的len相同
"""
# 封装/封包
a = 1, 2, 3
print(type(a))

# 解包
b, c, d = (1, 2, 3)
print(b, c, d)

# 可以用*号标识变量 接受多个变量
list01 = [1, 2, 3, 4, 5]

# y接受中间的[2,3,4]
x, *y, z = list01
print(x, y, z)

# 忽略中间的[2, 3, 4]
i, *_, j = list01
print(i, j)
